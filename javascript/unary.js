let num1 = 1
let num2 = 2

num1++   // Incrementa o valor de num1
console.log(num1)

--num1   // Decrementa o valor de num1
console.log(num1)

console.log(++num1 === num2--) // Incrementa o valor de num1 e compara com o valor de num2
console.log(num1 === num2)   // Compara o valor de num1 com o valor de num2